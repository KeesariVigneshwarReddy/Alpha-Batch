/* 13) Type promotion in java*/
import java.util.*;
public class LQ13
{
    public static void main(String args[]) 
    {
        Scanner sc = new Scanner(System.in);
        int a = 10;
        float b = 20.25f;
        long c = 25;
        double d = 30;
        System.out.println(a + b + c + d);
    }
}
