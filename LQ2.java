/* 2) Print number from 1 to 10 using while loop */
import java.util.*;
public class LQ2
{
    public static void main(String args[]) 
    {
        Scanner sc = new Scanner(System.in);
        int i = 1;
        while (i <= 10)
        {
            System.out.println(i);
            i++;
        }
    }
}
