/* 7) Product of a and b input from user */
import java.util.*;
public class LQ7
{
    public static void main(String args[]) 
    {
        Scanner sc = new Scanner(System.in);
        // input a
        int a = sc.nextInt();
        // input b
        int b = sc.nextInt();
        System.out.print("Product = ");
        // print product
        System.out.print(a * b);
    }
}
